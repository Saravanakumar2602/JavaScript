let a = -7.8;
let b = 16;
let nums = [3, 7, 2, 9, 4];

console.log("Math.abs(-7.8) =", Math.abs(a));        // 7.8
console.log("Math.ceil(-7.8) =", Math.ceil(a));      // -7
console.log("Math.floor(-7.8) =", Math.floor(a));    // -8
console.log("Math.round(-7.8) =", Math.round(a));    // -8
console.log("Math.trunc(-7.8) =", Math.trunc(a));    // -7

console.log("Math.sqrt(16) =", Math.sqrt(b));        // 4
console.log("Math.pow(2, 3) =", Math.pow(2, 3));     // 8

console.log("Math.min(...nums) =", Math.min(...nums)); // 2
console.log("Math.max(...nums) =", Math.max(...nums)); // 9


console.log("Math.PI =", Math.PI);                   // 3.141592653589793
console.log("Math.sin(0) =", Math.sin(0));           // 0
