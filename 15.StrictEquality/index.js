// =  Assignment Operator
// == Comparison Operator (Check the two values are equal)
// === Strict Equality Operator (Compares if values and data types are equal)
// != inequality Operator
// !== Strict Inequality Operator

let Pi = 3.14;  // It is a number datatype

if(Pi === "3.14")  // (==)checks only the value but (===)checks the data and also checks the datatype
{
    console.log("It is PI");
}else{
    console.log("It is not PI");
}